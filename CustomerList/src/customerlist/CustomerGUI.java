    public boolean isInteger( String input ) 
    { 
        try 
        {
            Integer.parseInt( input );
            return true; 
        }
        catch( NumberFormatException e ) 
        { 
            return false;
        }
    } 
    
    public boolean isLong( String input ) 
    { 
        try 
        {
            long tmp = Long.parseLong( input );
            int i=0;
            while((tmp/=10)>1)
                i++;
            if(i==9)
                return true; 
            else
                return false;
        }
        catch( NumberFormatException e ) 
        { 
            return false;
        }
    } 
    
