    public boolean verifyMonth(String tmonth)
    {
        String [] months={"January","February","March","April","May","June",
            "July","August","September","October","November","December"};
        int i;
        for(i=0;i<months.length;i++)
            if(tmonth.equals(months[i]))
                break;
        if(i<months.length)
            return true;//
        else
            return false;//
    }
    
