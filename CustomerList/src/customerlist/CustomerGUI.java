    public Customer searchByName(String nm)
    {
        //openCustomer();
        Customer tmp=null;
        int i=0,n=ls.size();
        System.out.println("LinkedList size "+n);
	while(i<n)
	{
            tmp=(Customer)ls.get(i);
            if(nm.equals(tmp.cnm))
                break;
            i++;
	}
        if(i==n)
        {
            JOptionPane.showMessageDialog(null,"There is no such name "
                    + "in the Database!\nPlease enter a valid Customer Name");
            tmp=null;
        }
        System.out.println(tmp.toString());
        return tmp;
    }
    
    public Customer searchByNo(int no)
    {
        //openCustomer();
        Customer tmp=null;
        int i=0,n=ls.size();
        System.out.println("LinkedList size "+n);
	while(i<n)
	{
            tmp=ls.get(i);
            if(no==tmp.cno)
                break;
            i++;
	}
        if(i==n)
        {
            JOptionPane.showMessageDialog(null,"There is no such Number "
                    + "in the Database!\nPlease enter a valid Customer Number");
            tmp=null;
        }
        return tmp;
    }
    
    public void openCustomer()
    {
        try
        {
            FileInputStream fi;
            ObjectInputStream oi;
            fi=new FileInputStream("CustomerList_DoNotDelete.amey");
            oi=new ObjectInputStream(fi);
            ls=(LinkedList <Customer>) oi.readObject();
            System.out.println("LinkedList size "+ls.size());
            fi.close();
            oi.close();
        }
        catch(IOException | ClassNotFoundException e)
        {
            JOptionPane.showMessageDialog(null,"Unable to find file...!!!"
                    + "\nRecreating the file...");
            try 
            {
                FileOutputStream fo=null;
                ObjectOutputStream oo=null;
                fo=new FileOutputStream("CustomerList_DoNotDelete.amey");
                oo=new ObjectOutputStream(fo);
                //oo.writeObject(ls);
                fo.close();
                oo.close();
            } 
            catch (FileNotFoundException ex) 
            {
                JOptionPane.showMessageDialog(null,"Unable to create file...!!!"
                        + "\nTry giving it another shot!");
            } 
            catch (IOException ex) 
            {
                JOptionPane.showMessageDialog(null,"Unable to write to the file...!!!"
                        + "\nTry giving it another shot!");
            }
            JOptionPane.showMessageDialog(null,"File is recreated!");
                    
        }
    }
    
